/**
 * Unicode Vertical Forms block with characters from uFE10 to uFE1F.
 * @author Kugel, <i>Theossos Comp Group</i>
 * @version Unicode 4.1.0
 */
public interface VerticalForms
{
   // constant field names modified with "^PRESENTATION FORM FOR VERTICAL " pattern 
   char COMMA = '\uFE10';
   char IDEOGRAPHIC_COMMA = '\uFE11';
   char IDEOGRAPHIC_FULL_STOP = '\uFE12';
   char COLON = '\uFE13';
   char SEMICOLON = '\uFE14';
   char EXCLAMATION_MARK = '\uFE15';
   char QUESTION_MARK = '\uFE16';
   char LEFT_WHITE_LENTICULAR_BRACKET = '\uFE17';
   char RIGHT_WHITE_LENTICULAR_BRAKCET = '\uFE18';
   char HORIZONTAL_ELLIPSIS = '\uFE19';
}
